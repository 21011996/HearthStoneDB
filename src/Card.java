public class Card {
    public Card(String name, int set, String flavor, String score, String clas, String type) {
        this.name = name;
        this.set = set;
        this.flavor = flavor;
        this.score = score;
        this.clas = clas;
        this.type = type;
    }

    String name;
    int set;
    String flavor;
    String score;
    String clas;
    String type;

}
